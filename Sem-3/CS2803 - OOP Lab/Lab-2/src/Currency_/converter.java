package Currency_;

public class converter {
    public static double DollarToInr(double curr){
        return curr * 83.72;
    }
    public static double InrToDollar(double curr){
        return curr / 83.72;
    }
    public static double EuroToInr(double curr){
        return curr * 90.79;
    }
    public static double InrToEuro(double curr){
        return curr / 90.79;
    }
    public static double YenToInr(double curr){
        return curr * 0.54;
    }
    public static double InrToYen(double curr){
        return curr / 0.54;
    }
}
